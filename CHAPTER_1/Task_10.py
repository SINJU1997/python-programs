#Expalain different types of data structure in python with a example also display the types of that data structure 

#List
number=[10,20,30,40]
numtype=type(number)
print("%s is a %s" %(number,numtype))
#Tuple
fruits=("apple","orange","banana")
fruitstype=type(fruits)
print("%s is a %s" %(fruits,fruitstype))
#set
name1={"sinju","anju","divya"}
nametype1=type(name1)
print("%s is a %s" %(name1,nametype1))
#dictionary
name2={
    "name":"sinju",
    "age":26
    }
nametype2=type(name2)
print("%s is a %s" %(name2,nametype2))
